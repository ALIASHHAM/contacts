function getResult() {
  var query = document.getElementById("text").value.toLowerCase();
  var result = "";
  var table = document.getElementById("table");
  table.innerHTML = ""; // Clear the table

  if (query.length >= 3) {
    for (var i = 0; i < country.length; i++) {
      var countrystr = country[i].toLowerCase();
      var codestr = code[i].toLowerCase();
      if (countrystr.includes(query) || codestr.includes(query)) {
        result += "<tr><td>" + country[i] + "</td><td>" + code[i] + "</td><td>" + counter[i] + "</td></tr>";
      }
    }
  }

  if (result.length !== 0) {
    table.innerHTML = "<tr><th>Name</th><th>Number</th><th>Counter</th></tr>" + result;
    table.style.display = "table"; // Show the table
  } else {
    table.innerHTML = "<tr><td colspan='3'>Not available</td></tr>";
    table.style.display = "table"; // Show the "Not available" message
  }

  if (query === "") {
    table.style.display = "none"; // Hide the table when the search bar is cleared
  }
}

